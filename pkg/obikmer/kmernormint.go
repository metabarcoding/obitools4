// NormalizeInt retourne le code du k-mer canonique (le plus petit lexicographiquement
// parmi toutes les permutations circulaires) pour un k-mer encodé en entier.
// Pour les k-mers de taille 1 à 6, utilise la table pré-calculée.
// Pour les k-mers plus grands, calcule à la volée.
func NormalizeInt(kmerCode int, kmerSize int) int {
	// Pour les k-mers de taille <= 6, utiliser la table
	if kmerSize <= 6 && kmerSize > 0 {
		if canonical, ok := LexicographicNormalizationInt[kmerSize][kmerCode]; ok {
			return canonical
		}
		// Si non trouvé dans la table (ne devrait pas arriver pour des k-mers valides)
	}

	// Pour les k-mers > 6 ou non trouvés, calculer les rotations circulaires
	return getCanonicalCircularInt(kmerCode, kmerSize)
}

// getCanonicalCircularInt retourne le code du plus petit k-mer lexicographiquement
// parmi toutes les permutations circulaires du k-mer encodé donné.
func getCanonicalCircularInt(kmerCode int, kmerSize int) int {
	if kmerSize <= 0 {
		return kmerCode
	}

	canonical := kmerCode
	mask := (1 << (kmerSize * 2)) - 1 // Masque pour garder k*2 bits
	shiftAmount := (kmerSize * 2) - 2 // Position du premier nucléotide

	// Générer toutes les permutations circulaires
	currentCode := kmerCode
	for i := 1; i < kmerSize; i++ {
		// Extraire le premier nucléotide (2 bits de poids fort)
		firstNuc := (currentCode >> shiftAmount) & 3
		// Décaler vers la gauche et ajouter le premier nucléotide à la fin
		currentCode = ((currentCode << 2) & mask) | firstNuc
		
		// Comparer lexicographiquement (le plus petit code est le plus petit lexicographiquement)
		if currentCode < canonical {
			canonical = currentCode
		}
	}

	return canonical
}

// EncodeKmer encode un k-mer (string) en entier selon le schéma de EncodeNucleotide.
// Cette fonction est utile pour les tests et le debug.
func EncodeKmer(kmer string) int {
	code := 0
	for i := 0; i < len(kmer); i++ {
		code = (code << 2) + int(EncodeNucleotide(kmer[i]))
	}
	return code
}

// DecodeKmer décode un entier en k-mer (string).
// Cette fonction est utile pour les tests et le debug.
func DecodeKmer(code int, kmerSize int) string {
	bases := []byte{'a', 'c', 'g', 't'}
	result := make([]byte, kmerSize)
	for i := kmerSize - 1; i >= 0; i-- {
		result[i] = bases[code&3]
		code >>= 2
	}
	return string(result)
}

// eulerTotient computes Euler's totient function φ(n), which counts
// the number of integers from 1 to n that are coprime with n.
func eulerTotient(n int) int {
	if n <= 0 {
		return 0
	}

	result := n

	// Process all prime factors
	for p := 2; p*p <= n; p++ {
		// Check if p is a prime factor
		if n%p == 0 {
			// Remove all occurrences of p
			for n%p == 0 {
				n /= p
			}
			// Apply the formula: φ(n) = n * (1 - 1/p) = n * (p-1)/p
			result -= result / p
		}
	}

	// If n is still greater than 1, then it's a prime factor
	if n > 1 {
		result -= result / n
	}

	return result
}

// divisors returns all divisors of n in ascending order.
func divisors(n int) []int {
	if n <= 0 {
		return []int{}
	}

	divs := []int{}
	for i := 1; i*i <= n; i++ {
		if n%i == 0 {
			divs = append(divs, i)
			if i != n/i {
				divs = append(divs, n/i)
			}
		}
	}

	// Sort in ascending order
	for i := 0; i < len(divs)-1; i++ {
		for j := i + 1; j < len(divs); j++ {
			if divs[i] > divs[j] {
				divs[i], divs[j] = divs[j], divs[i]
			}
		}
	}

	return divs
}

// necklaceCount computes the number of distinct necklaces (equivalence classes
// under rotation) for sequences of length n over an alphabet of size a.
// Uses Moreau's necklace-counting formula (also known as the necklace polynomial):
//
//	N(n, a) = (1/n) * Σ φ(d) * a^(n/d)
//
// where the sum is over all divisors d of n, and φ is Euler's totient function.
func necklaceCount(n, alphabetSize int) int {
	if n <= 0 {
		return 0
	}

	divs := divisors(n)
	sum := 0

	for _, d := range divs {
		// Compute a^(n/d)
		power := 1
		exp := n / d
		for i := 0; i < exp; i++ {
			power *= alphabetSize
		}

		sum += eulerTotient(d) * power
	}

	return sum / n
}

// CanonicalKmerCount returns the number of canonical k-mers (unique normalized forms)
// for a given k-mer size after circular normalization.
//
// For k=1 to 6, uses exact counts from pre-computed tables.
// For k>6, uses Moreau's necklace-counting formula for exact computation:
//
//	N(n, 4) = (1/n) * Σ φ(d) * 4^(n/d)
//
// where the sum is over all divisors d of n, and φ is Euler's totient function.
//
// These values are critical for calculating maximum entropy in entropy-based
// complexity filters, as circular normalization reduces the effective alphabet size.
func CanonicalKmerCount(kmerSize int) int {
	// Exact counts for k=1 to 6 (counted from normalization tables)
	switch kmerSize {
	case 1:
		return 4
	case 2:
		return 10
	case 3:
		return 24
	case 4:
		return 70
	case 5:
		return 208
	case 6:
		return 700
	default:
		// For k>6, use Moreau's necklace-counting formula for exact count
		// DNA alphabet has 4 bases
		return necklaceCount(kmerSize, 4)
	}
}
